package com.safeYourStep.yourSafeStepDTO;

public class UserShareInformationDTO {
	
	private String userStartingPoint;
	private String userDestinationPoint;
	private String userJourneyTime;
	private long userSafeRating;
	private String userJourneyDescription;
	
	
	
	public String getUserStartingPoint() {
		return userStartingPoint;
	}
	public void setUserStartingPoint(String userStartingPoint) {
		this.userStartingPoint = userStartingPoint;
	}
	public String getUserDestinationPoint() {
		return userDestinationPoint;
	}
	public void setUserDestinationPoint(String userDestinationPoint) {
		this.userDestinationPoint = userDestinationPoint;
	}
	public String getUserJourneyTime() {
		return userJourneyTime;
	}
	public void setUserJourneyTime(String userJourneyTime) {
		this.userJourneyTime = userJourneyTime;
	}
	public long getUserSafeRating() {
		return userSafeRating;
	}
	public void setUserSafeRating(long userSafeRating) {
		this.userSafeRating = userSafeRating;
	}
	public String getUserJourneyDescription() {
		return userJourneyDescription;
	}
	public void setUserJourneyDescription(String userJourneyDescription) {
		this.userJourneyDescription = userJourneyDescription;
	}
	
	
	
	

}
